/**
 * An interface for older tunes/ oldies
 * @author Mia Dia
 */
public interface Tune {


    /**
     * @return the artist's name
     */
    public String getArtistName();

     /**
     * @return song title
     */
    public String getDisplayTitle();

     /**
     * returns the category
     */
    public String getCategory();
}
